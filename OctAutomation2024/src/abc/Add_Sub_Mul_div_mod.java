package abc;

public class Add_Sub_Mul_div_mod {

	public static void main(String[] args) {

		int a = 40;
		int b = 20;
		 
		int add = a+b;
		int sub = a-b;
		int mul = a*b;
		int div = a/b;
		int mod = a%b;
		
		System.out.println("Addition of ab is =" + add);
		System.out.println("substraction of ab is =" + sub);
		System.out.println("Multiplication of ab is =" + mul);
		System.out.println("Division of ab is =" + div);
		System.out.println("Module of ab is =" + mod);

		
	}

}
