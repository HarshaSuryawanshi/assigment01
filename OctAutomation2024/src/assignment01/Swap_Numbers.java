public class Swap_Numbers {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		 int temp;
		 
		System.out.println("Before Swap" );
		System.out.print("a =" + a);
		System.out.println(" b =" + b);
		 temp=a;
		 a=b;
		 b=temp;
		System.out.println("After Swap" );
		System.out.print("a =" + a);
		System.out.println(" b =" + b);
		
	}

}
